package classpart08;

public class Class08 {
	
	public Class08() {};
	
	
	// 매개변수 x, y
	
	// 더하기 하는 함수 add()
	    public int add(int x, int y) {
	    	int result = x+y;
	    	return result;
	    }	
		
	// 빼기하는 함수 minus()
	    public int minus(int x, int y) {
	    	int result = x-y;
	    	return result;
	    }
		
	//곱하기 하는 함수 mulitply()
	    public int mulitply(int x, int y) {
	    	int result = x*y;
	    	return result;
	    }
		
	//나누기 하는 함수 divid()
	    public int divid(int x, int y) {
	    	int result = x/y;
	    	return result;
	    }
		
}
